package club.gclmit.chaos.storage.service;

import club.gclmit.chaos.core.exception.ChaosException;
import club.gclmit.chaos.core.id.IdUtils;
import club.gclmit.chaos.core.io.FileTypeUtils;
import club.gclmit.chaos.core.io.FileUtils;
import club.gclmit.chaos.core.io.MimeType;
import club.gclmit.chaos.core.utils.SecureUtils;
import club.gclmit.chaos.core.utils.StringUtils;
import club.gclmit.chaos.storage.pojo.CloudStorage;
import club.gclmit.chaos.storage.pojo.FileInfo;
import cn.hutool.core.date.DateUtil;
import org.springframework.util.Assert;
import org.springframework.web.multipart.MultipartFile;

import java.io.ByteArrayInputStream;
import java.io.File;
import java.io.FileInputStream;
import java.io.InputStream;
import java.nio.charset.StandardCharsets;
import java.time.LocalDate;
import java.time.format.DateTimeFormatter;
import java.util.Collections;

/**
 * <p>
 * 抽象存储类
 * </p>
 *
 * @author gclm
 */
public abstract class AbstractStorageClient implements CloudStorageClient {

	/**
	 * 云存储配置信息
	 */
	protected CloudStorage cloudStorage;

	public AbstractStorageClient(CloudStorage cloudStorage) {
		this.cloudStorage = cloudStorage;
	}


	/**
	 * <p>
	 * 上传文件
	 * </p>
	 *
	 * @param file 文件
	 * @return {@link FileInfo} 文件信息
	 * @author 孤城落寞
	 */
	@Override
	public FileInfo upload(File file) {
		Assert.isTrue(file.exists(), "上传文件不能为空");
		try {
			FileInputStream fileInputStream = new FileInputStream(file);
			return upload(fileInputStream, buildFileInfo(file));
		} catch (Exception e) {
			throw new ChaosException("文件上传失败", e);
		}
	}

	/**
	 * <p>
	 * 上传文件
	 * </p>
	 *
	 * @param file 文件
	 * @return {@link FileInfo} 文件信息
	 * @author 孤城落寞
	 */
	@Override
	public FileInfo upload(MultipartFile file) {
		try {
			InputStream stream = file.getInputStream();
			return upload(stream, buildFileInfo(file));
		} catch (Exception e) {
			throw new ChaosException("文件上传失败", e);
		}
	}

	/**
	 * <p>
	 * 上传字节数组
	 * </p>
	 *
	 * @param data     字节数组
	 * @param key      文件路径
	 * @param fileName 文件名
	 * @return {@link FileInfo} 文件信息
	 * @author 孤城落寞
	 */
	@Override
	public FileInfo upload(byte[] data, String key, String fileName) {
		Assert.notEmpty(Collections.singleton(data), "上传文件失败，请检查 byte[] 是否正常");
		Assert.hasLength(key, "上传文件失败，请检查上传文件的 key 是否正常");

		/*
		 * 根据工具类获取 fileInfo 参数
		 */
		String contentType = MimeType.TXT.getMimeType();
		String md5 = SecureUtils.md5(data);
		Long size = Long.valueOf(String.valueOf(data.length));

		/*
		 * 如果不制定文件名,则key为文件名
		 */
		if (StringUtils.isBlank(fileName)) {
			fileName = key;
		}

		return upload(new ByteArrayInputStream(data), new FileInfo(fileName, contentType, size, md5, key, cloudStorage.getType().getCode()));
	}

	/**
	 * 上传字符串
	 *
	 * @param content  字符串内容
	 * @param key      key
	 * @param fileName 文件名
	 * @return {@link FileInfo} 文件信息
	 * @author gclm
	 */
	@Override
	public FileInfo upload(String content, String key, String fileName) {
		Assert.hasLength(key, "上传文件失败，请检查上传文件的 content 是否正常");
		Assert.hasLength(key, "上传文件失败，请检查上传文件的 key 是否正常");

		if (StringUtils.isBlank(key)) {
			key = DateUtil.current() + ".txt";
		}
		return upload(content.getBytes(StandardCharsets.UTF_8), key, fileName);
	}

	/**
	 * 构造FileInfo
	 *
	 * @param file 文件
	 * @return {@link FileInfo} 文件信息
	 * @author gclm
	 */
	public FileInfo buildFileInfo(File file) {
		String key = getPath(cloudStorage.getConfig().getPrefix(), FileUtils.getSuffix(file));
		String contentType = FileTypeUtils.getMimeType(file);
		String md5 = SecureUtils.md5(file);
		return new FileInfo(file.getName(), contentType, file.length(), md5, key, cloudStorage.getType().getCode());
	}

	/**
	 * 构造FileInfo
	 *
	 * @param file 文件
	 * @return {@link FileInfo} 文件信息
	 * @author gclm
	 */
	public FileInfo buildFileInfo(MultipartFile file) {
		String key = getPath(cloudStorage.getConfig().getPrefix(), FileTypeUtils.getSuffix(file));
		return new FileInfo(file.getOriginalFilename(), file.getContentType(), file.getSize(), SecureUtils.md5(file), key, cloudStorage.getType().getCode());
	}

	/**
	 * 文件路径
	 * <p>
	 * 这里采用的文件命名格式：时间段 + 全局id
	 *
	 * @param prefix 前缀
	 * @param suffix 后缀
	 * @return {@link String} oss key
	 */
	public String getPath(String prefix, String suffix) {

		LocalDate localDate = LocalDate.now();
		String dateFormat = localDate.format(DateTimeFormatter.BASIC_ISO_DATE);

		//文件路径
		StringBuilder path = new StringBuilder();

		/*
		 * 前后缀拼接逻辑:
		 *  先判断是否存在前缀，存在在拼接，否则根据实际情况进行修改
		 */
		if (StringUtils.isNotBlank(prefix)) {
			path.append(prefix).append("/");
		}

		path.append(dateFormat).append("/").append(IdUtils.getYeinGid());

		if (suffix != null) {
			if (suffix.contains(StringUtils.DOT)) {
				path.append(suffix);
			} else {
				path.append(StringUtils.DOT).append(suffix);
			}
		}
		return path.toString();
	}
}
